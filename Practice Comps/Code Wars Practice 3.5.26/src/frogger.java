import java.util.*;
import java.io.*;


public class frogger{

    public void run() throws FileNotFoundException{
        Scanner f= new Scanner(new File("frogger".toLowerCase()+".dat"));

        while(f.hasNext()){
            f.next();
            char[][] map = new  char[10][10];

            map[0] = "0".repeat(10).toCharArray();
            map[9] = "0".repeat(10).toCharArray();


            for (int r = 1; r < 9; r++) {
                for (int c = 0; c < 10; c++) {
                    map[r][c] = f.next().charAt(0);
                }
            }
            f.next();

            possible = false;

            for (int c = 0; c < 10; c++){
                solve(map, 0, c);
//                System.out.println();
            }

            if (possible) System.out.println("LEFTOVER POSSUM");
            else System.out.println("FROGGER");
        }

        f.close();
    }

    boolean possible;

    public void solve(char[][] map, int row, int col){
        if (row == 9){
            possible = true;
            return;
        }

        if (map[row][col] != '0') return;

        char[][] copy = new char[10][10];
        for (char[] each:copy) Arrays.fill(each, '0');

        for (int r = 0; r < 10; r++){
            for (int c = 0; c < 10; c++){
                if (map[r][c] != '0'){
                    int sign = (r <= 4 ? -1 : 1);
//                    diff *= map[r][c] - '0';
//
//                    int newC = c + diff;
//                    newC = (newC + 10) % 10;
//                    copy[r][newC] = map[r][c];
//                    if (r == row && newC == col) return;

                    for (int i = 0; i <= map[r][c]-'0'; i++) {
                        int tempC = sign * i + c;
                        tempC = (tempC+10)%10;
                        if (r == row && tempC == col) return;
                        if (i == map[r][c] - '0'){
                            copy[r][tempC] = map[r][c];
                        }
                    }
                }
            }
        }

        if (copy[row][col] != '0') return;

//        for (char[] each:copy) System.out.println(each);
//        System.out.println(row+" "+col);
//        System.out.println("-".repeat(10));


        solve(copy, row+1, col);
    }



    public static void main(String[] args) throws FileNotFoundException{
        new frogger().run();
    }
}

